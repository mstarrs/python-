# первый закон Менделя: вероятность доминантного фенотипа у потомка

# ввод данных
data = input().split()
k = int(data[0])  # гомозиготные доминантные (AA)
m = int(data[1])  # гетерозиготные (Aa)
n = int(data[2])  # гомозиготные рецессивные (aa)

tot = k + m + n  # всего особей

# вероятность получить рецессивного потомка (aa) для каждой пары родителей

# Случай 1: aa x aa -> 100% рецессивный
p1 = (n / tot) * ((n - 1) / (tot - 1)) * 1.0

# Случай 2: aa x Aa -> 50% рецессивный
p2 = (n / tot) * (m / (tot - 1)) * 0.5

# Случай 3: Aa x aa -> 50% рецессивный
p3 = (m / tot) * (n / (tot - 1)) * 0.5

# Случай 4: Aa x Aa -> 25% рецессивный
p4 = (m / tot) * ((m - 1) / (tot - 1)) * 0.25

# все остальные пары (AA с любым) дают 0% рецессивных

# суммируем вероятности получить рецессивного потомка
rec = p1 + p2 + p3 + p4

# вероятность доминантного фенотипа = 1 - вероятность рецессивного
dom = 1 - rec

# выводим результат (с 5 знаками после запятой, так как в примере тоже 5 знаков)
print(f"{dom:.5f}")